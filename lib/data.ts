// Helper functions
export function formatRupiah(value: number): string {
  return new Intl.NumberFormat("id-ID", {
    style: "currency",
    currency: "IDR",
    minimumFractionDigits: 0,
    maximumFractionDigits: 0,
  }).format(value)
}

export function buildTrend(province: ProvinceData) {
  const hist = province.historicalPrices ?? []
  const n = hist.length

  const historical = hist.map((price, i) => ({
    week: i === n - 1 ? "Minggu Ini" : `M-${n - 1 - i}`,
    actual: price,
    predicted: undefined,
  }))

  const predicted = province.prediksi12Minggu.map((price, i) => ({
    week: `M+${i + 1}`,
    actual: undefined,
    predicted: price,
  }))

  return [...historical, ...predicted]
}
